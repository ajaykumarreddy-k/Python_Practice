#Array 
arr = [10, 20, 30, 40, 50]
#Accessing elements
print(arr[0])  # Output: 10 
#update elements
arr[1] = 25
print(arr)  # Output: [10, 25, 30, 40,
#Traversing an array
for i in arr:
    print(i)  # Output: 10 25 30 40 50


# What we are doning in this code is is first adding an array and printing the first element, then updating the second element and printing the whole array, and finally traversing the array and printing each element.
#here for loop helps for reading the elements of the array one by one.